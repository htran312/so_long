#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	ch;

	ch = c;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ch)
			return ((char *)&str[i]);
		i++;
	}
	if (str[i] == ch)
		return ((char *) &str[i]);
	else
		return (NULL);
}
